    public String generarID(){
        String id = "";
        String save = "";
        int secuencia = 0;
        for(int i = 0; i<3; i++){
            id = id+Character.toUpperCase(this.txtNombre.getText().charAt(i));
        }
        
        save = id;
        do{
            secuencia++;
            if(secuencia<10){
                id = save+"0"+secuencia;
            }else{
                id = save+secuencia;
            }
            
        }while(id.equals(buscarID(id)));
        System.out.println("pasaste la secuencia");
        return id;
    }
    
    private String buscarID(String id){
        String resultado = "";
        try{
        Statement st = cn.createStatement();
        String recordQuery = ("SELECT * FROM Vendedores");
        ResultSet rs = st.executeQuery(recordQuery);
        while(rs.next()){
            if(id.equals(rs.getString("id_vendedor"))){
                resultado = id;
            }
        }
        return resultado;
        }catch(Exception e){
            return "";
        }
    }
    
    public void registrarVendedor(){
        try{
        PreparedStatement pps = cn.prepareStatement("INSERT INTO Vendedores VALUES(?,?,?,?)");
        pps.setString(1, generarID());
        pps.setString(2, this.txtNombre.getText());
        pps.setString(3, this.txtApellidoP.getText());
        pps.setString(4, this.txtApellidoM.getText());
        pps.execute();
        }catch(Exception e){
            System.out.println("error");
        }
        
    }
    
